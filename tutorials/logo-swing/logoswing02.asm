	SCREEN_RAM = $0400
	EFFECT_COUNTER = $02
	CHAR_OFFSET = $03
	PIXEL_OFFSET = $04
	

	+BASIC_START

	;-----------------------------------------
	; MAIN
	;-----------------------------------------
	
	* = $c000

	+CLEAR_SCREEN
	
	lda #00
	sta $d020
	sta $d021
	
	;Set raster irq
 	sei

	lda #$7f	;Disable CIA I and CIA II interrupts
	sta $dc0d

	lda $d01a	;Enable raster interrupts
	ora #$01
	sta $d01a

	lda #$00
	sta $dc0e
	
	lda #154 	;set raster line
	sta $d012
	lda $d011
	and #$7f 	;%01111111
    	sta $d011
		
	lda #<irq 	;set interrupt handler
	sta $0314
	lda #>irq
	sta $0315
 
    	cli
    	
    	jmp *
        	
        ;-----------------------------------------------------
        ; IRQ ROUTINE
        ;-----------------------------------------------------

irq     inc EFFECT_COUNTER 	;increase effect counter
	ldx EFFECT_COUNTER 	;and grab swing data
	Lda swing_data,x
	
	lsr 		;Divide by 8 and
	lsr 		;store as char ofsset
	lsr
	sta CHAR_OFFSET
	
	lda swing_data,x
	and #$07
	sta PIXEL_OFFSET
	
	jsr updateLogo
	
	asl $d019			
	jmp $ea81		

updateLogo	
!zone {	;Pixel Scroll
	lda PIXEL_OFFSET
	and #$07 	;%00000111
	sta $d016
	
	;Char Scroll
	ldx CHAR_OFFSET
	ldy #00
-	lda logo,y
	sta SCREEN_RAM+40*5,x
	lda logo+160,y
	sta SCREEN_RAM+40*9,x
	inx
	iny
	cpx #160
	bne - 
	
	rts
}	

